/*
 * Vendored numeric ids for the LEGACY IEEE 802.15.4 MAC generic netlink
 * family (kernel net/ieee802154/netlink.c, family name "802.15.4 MAC").
 * The kernel-side enum lives in include/linux/nl802154.h which is a
 * kernel-internal header -- it is NOT installed via uapi, so the
 * sysroot has no <linux/nl802154.h> that names these IEEE802154_*
 * symbols.  Note this is a distinct family from the modern "nl802154"
 * (CONFIG_IEEE802154_NL802154) family vendored in kernel/nl802154.h;
 * the two share a kernel header path but expose different command
 * and attribute namespaces and register as separate genl families.
 *
 * The fallback values below mirror the ordering of enum
 * { IEEE802154_ATTR_* } and enum { IEEE802154_*_REQ / *_RESP / LIST_* /
 * ADD_* / DEL_* / SET_MACPARAMS / LLSEC_* } in
 * include/linux/nl802154.h.  The kernel-side enum has shipped since
 * the original 2.6.32 ieee802154 socket support and the order is
 * load-bearing for the userspace ABI: any reordering would break the
 * dispatcher's small_ops index.  If a value ever drifts the kernel
 * returns -EOPNOTSUPP / -EINVAL on the relevant request and the
 * per-family cap-gate latches.
 */

